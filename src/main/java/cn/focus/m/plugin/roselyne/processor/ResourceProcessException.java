package cn.focus.m.plugin.roselyne.processor;

public class ResourceProcessException extends Exception {

    private static final long serialVersionUID = 4935171490995583595L;

    public ResourceProcessException() {
        super();
    }

    public ResourceProcessException(String message, Throwable cause) {
        super(message, cause);
    }

    public ResourceProcessException(String message) {
        super(message);
    }

    public ResourceProcessException(Throwable cause) {
        super(cause);
    }

}
